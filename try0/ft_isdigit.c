int ft_isdigit(int c)
{
	if (( c >= '0' && c <= '9'))
		return (1);
	else
		return (0);
}

int main(void)
{
	#include <stdio.h>

	int eje = 'r';

	printf("%d", ft_isdigit(eje));
}
